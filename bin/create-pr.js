#!/usr/bin/env node
// Standalone PR creation script for the orchestrator
// Usage: create-pr.js <title> <branch> <issue_number>
// Uses the same App + token approach as github-mcp-server.js

const crypto = require('crypto');
const https = require('https');
const fs = require('fs');
const path = require('path');

const HOME = process.env.HOME || '/Users/aetherclaude';
const UPSTREAM_REPO = 'ten9876/AetherSDR';
// No fork — push and PR are on the same upstream repo

function loadEnv() {
    const env = {};
    try {
        fs.readFileSync(path.join(HOME, '.env'), 'utf8').split('\n').forEach(l => {
            if (l && !l.startsWith('#') && l.includes('=')) {
                const [k, ...v] = l.split('=');
                env[k.trim()] = v.join('=').trim();
            }
        });
    } catch(e) {}
    return env;
}

function makeJWT(env) {
    const pk = fs.readFileSync(path.join(HOME, '.github-app-key.pem'), 'utf8');
    const now = Math.floor(Date.now() / 1000);
    const h = Buffer.from('{"alg":"RS256","typ":"JWT"}').toString('base64url');
    const p = Buffer.from(JSON.stringify({iat: now - 60, exp: now + 600, iss: env.GITHUB_APP_ID})).toString('base64url');
    const s = crypto.createSign('RSA-SHA256');
    s.update(`${h}.${p}`);
    return `${h}.${p}.${s.sign(pk, 'base64url')}`;
}

function apiCall(method, apiPath, body, token, bearer) {
    return new Promise((resolve, reject) => {
        const data = body ? JSON.stringify(body) : null;
        const opts = {
            hostname: 'api.github.com',
            path: apiPath,
            method: method,
            headers: {
                'Authorization': `${bearer ? 'Bearer' : 'token'} ${token}`,
                'Accept': 'application/vnd.github+json',
                'User-Agent': 'AetherClaude-PR/1.0',
                'Content-Type': 'application/json'
            }
        };
        if (data) opts.headers['Content-Length'] = Buffer.byteLength(data);

        const req = https.request(opts, res => {
            let body = '';
            res.on('data', c => body += c);
            res.on('end', () => {
                try {
                    const parsed = JSON.parse(body);
                    if (res.statusCode >= 400) {
                        reject(new Error(`HTTP ${res.statusCode}: ${body.substring(0, 500)}`));
                    } else {
                        resolve(parsed);
                    }
                } catch(e) {
                    reject(new Error(`Parse error: ${body.substring(0, 200)}`));
                }
            });
        });
        req.on('error', reject);
        if (data) req.write(data);
        req.end();
    });
}

async function main() {
    const title = process.argv[2];
    const branch = process.argv[3];
    const issueNumber = process.argv[4];
    const bodyFile = process.argv[5];

    if (!title || !branch || !issueNumber) {
        console.error('Usage: create-pr.js <title> <branch> <issue_number> [body_file]');
        process.exit(1);
    }

    let prBody;
    if (bodyFile && fs.existsSync(bodyFile)) {
        prBody = fs.readFileSync(bodyFile, 'utf8');
        try { fs.unlinkSync(bodyFile); } catch(e) {}
    } else {
        prBody = `## Summary\n\nFixes #${issueNumber}\n\n---\nGenerated by AetherClaude (automated agent for AetherSDR)`;
    }

    const env = loadEnv();
    const jwt = makeJWT(env);
    const installs = await apiCall('GET', '/app/installations', null, jwt, true);
    const perms = {permissions: {contents: 'write', pull_requests: 'write', issues: 'write', metadata: 'read'}};

    // Try each installation until one succeeds
    const installOrder = [
        installs.find(i => i.account.login === 'ten9876'),
        installs.find(i => i.account.login === 'AetherClaude')
    ].filter(Boolean);

    let lastError = '';
    for (const inst of installOrder) {
        try {
            const tok = await apiCall('POST', `/app/installations/${inst.id}/access_tokens`, perms, jwt, true);
            const pr = await apiCall('POST', `/repos/${UPSTREAM_REPO}/pulls`, {
                title: title,
                body: prBody,
                head: branch,
                base: 'main',
                draft: true
            }, tok.token, false);

            // Enable GitHub's native auto-merge so the PR squash-merges itself
            // once it leaves draft, gets approved, and CI passes. Best-effort —
            // if the repo doesn't permit auto-merge or required checks aren't
            // configured, log and continue (PR creation still succeeded).
            const autoMergeMutation =
                'mutation($id: ID!) { ' +
                '  enablePullRequestAutoMerge(input: {pullRequestId: $id, mergeMethod: SQUASH}) { ' +
                '    pullRequest { autoMergeRequest { enabledAt } } ' +
                '  } ' +
                '}';
            let autoMergeStatus = 'enabled';
            try {
                const gql = await apiCall('POST', '/graphql',
                    {query: autoMergeMutation, variables: {id: pr.node_id}},
                    tok.token, false);
                if (gql.errors && gql.errors.length) {
                    autoMergeStatus = 'failed: ' + gql.errors.map(e => e.message).join('; ');
                }
            } catch(e) {
                autoMergeStatus = 'failed: ' + e.message;
            }

            console.log(JSON.stringify({number: pr.number, url: pr.html_url, autoMerge: autoMergeStatus}));
            return;
        } catch(e) {
            lastError = e.message;
        }
    }
    console.log(JSON.stringify({error: lastError}));
    process.exit(1);
}

main().catch(e => {
    console.log(JSON.stringify({error: e.message}));
    process.exit(1);
});
